
<?php
/*Affichage des entiers de 1 à 10 à l'aide d'une boucle while*/
$n = 1;
while ($n<=10) {
    echo "$n <br>";
    $n++;
}
/*Affichage des entiers de 1 à 10 à l'aide d'une boucle for*/
for ($n = 1; $n <= 10; $n++) {
    echo"$n <br>";
}
